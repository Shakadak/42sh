int			ft_strcmp(char *s1, char *s2)
{
	int		a;

	if (s1 == 0 || s2 == 0)
		return (0);
	a = 0;
	while (s1[a] != 0 || s2[a] != 0)
	{
		if (s1[a] < s2[a])
			return (0);
		else if (s1[a] > s2[a])
			return (0);
		a = a + 1;
	}
	if (s1[a] != 0 || s2[a] != 0)
		return (0);
	return (1);
}
