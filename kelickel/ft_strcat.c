char		*ft_strcat(char *s1, const char *s2)
{
	int		a;
	int		b;

	if (s1 == 0 || s2 == 0)
		return (s1);
	a = 0;
	b = 0;
	while (s1[b] != 0)
		b = b + 1;
	while (s2[a] != 0)
	{
		s1[b] = s2[a];
		a = a + 1;
		b = b + 1;
	}
	s1[b] = 0;
	return (s1);
}
